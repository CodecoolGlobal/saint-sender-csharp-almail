        /// <summary>
        /// Changes the alpha value of a color and returns it.
        /// </summary>
        /// <param name="color">Base color</param>
        /// <param name="alpha">The alpha (between 0 and 1)</param>
        /// <param name="rgbMultiplier">The Red, Green, Blue component's multiplier (between 0 and 1)</param>
        /// <returns>Alpha changed color</returns>
        public static Color ColorAlpha(Color color, float alpha = 1, float rgbMultiplier = 1)
        {
            return Color.FromArgb((byte)Math.Round(alpha * 255), (byte)(color.R * rgbMultiplier), (byte)(color.G * rgbMultiplier), (byte)(color.B * rgbMultiplier));
        }

        public static string TextMaxWidth(string text, int maxWidth, string suffix = "...")
        {
            if (text.Length <= maxWidth)
                return text;

            string[] words = text.Split(' ');
            string result = "";
            foreach(string word in words)
            {
                if ((result + " " + word).Length > maxWidth)
                    break;

                result += " " + word;
            }

            if (result.Length < maxWidth / 2)
                return result.Substring(0, Math.Min(maxWidth, result.Length)) + suffix;

            return result + suffix;
        }

        public static string TextMaxHeight(string text, int lines, string suffix = "[...]", int maxWidth = -1)
        {
            string[] textLines = text.Split('\n');
            string[] resultLines = new string[Math.Min(textLines.Length, lines)];
            for(int i=0; i <resultLines.Length; i++)
            {
                resultLines[i] = maxWidth < 0 ? textLines[i] : TextMaxWidth(textLines[i], maxWidth);
            }
            return string.Join("\n", resultLines);
        }

        /// <summary>
        /// Creates a GradientStopCollection with evenly distributed colors
        /// </summary>
        /// <param name="colors">The array of the colors in order</param>
        /// <returns>Evenly distributed GradientStopCollection</returns>
        public static GradientStopCollection Gradient(Color[] colors)
        {
            GradientStopCollection stops = new GradientStopCollection(colors.Length);

            if (colors.Length == 0)
                return stops;

            double ratio = 1.0 / (colors.Length - 1);
            for (int i = 0; i < colors.Length; i++)
                stops.Add(new GradientStop(colors[i], ratio * i));
            return stops;
        }


